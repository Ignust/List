#ifndef CLIST_HPP
#define CLIST_HPP

#include "CNode.hpp"

template<typename T>
class CList
{
public:
    CList();

    void push_back(T date);
    int getSize();
private:

    int mSize;
    CNode<T> *mHead;

};


template<typename T>
////////////////////////////////////////////////////////////////////////////////////
CList<T>::CList()
    : mSize(0),
      mHead(nullptr)
////////////////////////////////////////////////////////////////////////////////////
{

}
template<typename T>
////////////////////////////////////////////////////////////////////////////////////
void CList<T>::push_back(T date)
////////////////////////////////////////////////////////////////////////////////////
{
    if (mHead == nullptr){
        mHead =  new CNode<T>(date);
    } else{
        CNode<T> *current = mHead;
        while (current->mPtrNextNode != nullptr) {
            current = current->mPtrNextNode;
        }
        current->mPtrNextNode = new CNode<T>(date);
    }
    mSize++;
}
template<typename T>
////////////////////////////////////////////////////////////////////////////////////
int CList<T>::getSize()
////////////////////////////////////////////////////////////////////////////////////
{
    return mSize;
}

#endif // CLIST_HPP
